class formatter:

    def newLine():
        return '\n'

    def singleIndentation():
        return '\t'

    def doubleIndentation():
        return '\t\t'

    def tripleIndentation():
        return '\t\t\t'

    def quadrupleIndentation():
        return '\t\t\t\t'

    def quintupleIndentation():
        return '\t\t\t\t\t'
